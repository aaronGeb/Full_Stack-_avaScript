// Arithmetic Operators
let a = 10;
let b = 90
console.log(a + b) // 100
console.log(a - b) // -80
console.log(a % b) // 10
console.log(a * b) // 900
console.log(a / b) // 0.1111111111111111


// Assignment Operators

let x = 10;
x += 10; // x = x + 10
console.log(x) // 20


// increment and decrement operators
let y = 10;
y++;
console.log(y) // 11
y--;
console.log(y) // 10
// Unary Operators
let z = 10;
z = -z;
console.log(z) // -10
// Logical Operators
let isTrue = true;
let isFalse = false;
console.log(isTrue && isFalse) // false
console.log(isTrue || isFalse) // true
